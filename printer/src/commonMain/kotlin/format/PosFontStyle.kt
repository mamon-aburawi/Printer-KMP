package format

enum class PosFontStyle(val bytes: ByteArray) {


    NORMAL(byteArrayOf(0x1B, 0x45, 0x00, 0x1B, 0x2D, 0x00)),

    BOLD(byteArrayOf(0x1B, 0x45, 0x01, 0x1B, 0x2D, 0x00)),

    UNDER_LINE(byteArrayOf(0x1B, 0x45, 0x00, 0x1B, 0x2D, 0x01)),

    INVERSE(byteArrayOf(0x1D, 0x42, 0x01))

}
